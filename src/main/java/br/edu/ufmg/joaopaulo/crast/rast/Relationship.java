package br.edu.ufmg.joaopaulo.crast.rast;

public class Relationship {
	
	private Long n1;
	private Long n2;
	private String type;
	
	public Long getN1() {
		return n1;
	}
	public void setN1(Long n1) {
		this.n1 = n1;
	}
	public Long getN2() {
		return n2;
	}
	public void setN2(Long n2) {
		this.n2 = n2;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
